-- Función para actualizar el campo updated_at automáticamente
CREATE OR REPLACE FUNCTION gd_update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Tipos para los estados de los vehículos con prefijo
DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'gd_vehicle_status_type') THEN
        CREATE TYPE gd_vehicle_status_type AS ENUM ('operativo', 'demora', 'standby', 'mtto');
    END IF;
END $$;

-- Tabla de Vehículos con prefijo
CREATE TABLE IF NOT EXISTS gd_vehicles (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    patente TEXT UNIQUE NOT NULL,
    modelo TEXT,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    deleted_at TIMESTAMPTZ
);

CREATE TRIGGER update_gd_vehicles_updated_at BEFORE UPDATE ON gd_vehicles FOR EACH ROW EXECUTE PROCEDURE gd_update_updated_at_column();

-- Tabla de Estado Actual y Ubicación con prefijo
CREATE TABLE IF NOT EXISTS gd_vehicle_status (
    vehicle_id UUID PRIMARY KEY REFERENCES gd_vehicles(id) ON DELETE CASCADE,
    status gd_vehicle_status_type DEFAULT 'standby',
    lat FLOAT8,
    lng FLOAT8,
    updated_by UUID REFERENCES auth.users(id),
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    deleted_at TIMESTAMPTZ
);

CREATE TRIGGER update_gd_vehicle_status_updated_at BEFORE UPDATE ON gd_vehicle_status FOR EACH ROW EXECUTE PROCEDURE gd_update_updated_at_column();

-- Tabla de Historial de Estados con prefijo
CREATE TABLE IF NOT EXISTS gd_status_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id UUID REFERENCES gd_vehicles(id) ON DELETE CASCADE,
    status gd_vehicle_status_type NOT NULL,
    lat FLOAT8,
    lng FLOAT8,
    created_by UUID REFERENCES auth.users(id),
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    deleted_at TIMESTAMPTZ
);

CREATE TRIGGER update_gd_status_logs_updated_at BEFORE UPDATE ON gd_status_logs FOR EACH ROW EXECUTE PROCEDURE gd_update_updated_at_column();

-- Habilitar Realtime para la tabla gd_vehicle_status
ALTER PUBLICATION supabase_realtime ADD TABLE gd_vehicle_status;

-- Tabla de Roles
CREATE TABLE IF NOT EXISTS gd_roles (
    id SERIAL PRIMARY KEY,
    name TEXT UNIQUE NOT NULL, -- 'admin', 'driver', 'dispatcher'
    description TEXT,
    created_at TIMESTAMPTZ DEFAULT now()
);

