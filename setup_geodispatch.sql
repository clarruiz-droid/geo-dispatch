-- Tabla de Perfiles de Usuario
CREATE TABLE IF NOT EXISTS gd_profiles (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT UNIQUE NOT NULL,
    full_name TEXT,
    role_id INTEGER REFERENCES gd_roles(id) DEFAULT (SELECT id FROM gd_roles WHERE name = 'driver'),
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now(),
    deleted_at TIMESTAMPTZ
);

CREATE TRIGGER update_gd_profiles_updated_at BEFORE UPDATE ON gd_profiles FOR EACH ROW EXECUTE PROCEDURE gd_update_updated_at_column();

-- Habilitar RLS para perfiles
ALTER TABLE gd_profiles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow users to read their own profile" ON gd_profiles FOR SELECT USING (auth.uid() = id);
CREATE POLICY "Allow admins to read all profiles" ON gd_profiles FOR SELECT TO authenticated USING (
    EXISTS (
        SELECT 1 FROM gd_profiles p 
        JOIN gd_roles r ON p.role_id = r.id 
        WHERE p.id = auth.uid() AND r.name = 'admin'
    )
);

-- Trigger para crear perfil automáticamente al registrarse en Auth
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO public.gd_profiles (id, email, full_name, role_id)
    VALUES (
        new.id, 
        new.email, 
        COALESCE(new.raw_user_meta_data->>'full_name', 'Nuevo Usuario'),
        (SELECT id FROM gd_roles WHERE name = 'driver') -- Por defecto todos son choferes
    );
    RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Eliminar el trigger si ya existe para evitar errores al re-ejecutar
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();
