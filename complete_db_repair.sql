-- ========================================================
-- SCRIPT: REPARACIÓN INTEGRAL DE ESTRUCTURA GPS (v0.5.3)
-- ========================================================
-- Este script asegura que todas las columnas y relaciones 
-- necesarias para el GPS y los nombres existan.

-- 1. Asegurar columnas en gd_vehicle_status
ALTER TABLE gd_vehicle_status ADD COLUMN IF NOT EXISTS updated_by UUID REFERENCES gd_profiles(id);
ALTER TABLE gd_vehicle_status ADD COLUMN IF NOT EXISTS is_emergency BOOLEAN DEFAULT false;
ALTER TABLE gd_vehicle_status ADD COLUMN IF NOT EXISTS lat FLOAT8;
ALTER TABLE gd_vehicle_status ADD COLUMN IF NOT EXISTS lng FLOAT8;

-- 2. Asegurar estructura de gd_gps_history
CREATE TABLE IF NOT EXISTS gd_gps_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id UUID REFERENCES gd_vehicles(id) ON DELETE CASCADE,
    profile_id UUID REFERENCES gd_profiles(id) ON DELETE SET NULL,
    lat FLOAT8 NOT NULL,
    lng FLOAT8 NOT NULL,
    accuracy FLOAT8,
    captured_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 3. Habilitar Realtime para la tabla de estados (CRUCIAL PARA EL MAPA)
-- Esto permite que el mapa se mueva sin recargar
ALTER TABLE gd_vehicle_status REPLICA IDENTITY FULL;
DROP PUBLICATION IF EXISTS supabase_realtime;
CREATE PUBLICATION supabase_realtime FOR TABLE gd_vehicle_status;

-- 4. Permisos de RLS para el GPS
ALTER TABLE gd_gps_history ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Choferes insertan su GPS" ON gd_gps_history;
CREATE POLICY "Choferes insertan su GPS" 
ON gd_gps_history FOR INSERT 
TO authenticated 
WITH CHECK (auth.uid() = profile_id);

DROP POLICY IF EXISTS "Todos ven el historial" ON gd_gps_history;
CREATE POLICY "Todos ven el historial" 
ON gd_gps_history FOR SELECT 
TO authenticated 
USING (true);

-- 5. Asegurar que los perfiles son legibles por todos los autenticados (para los nombres en el mapa)
GRANT SELECT ON gd_profiles TO authenticated;
